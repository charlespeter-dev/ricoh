    <script>

        window.addEventListener('load', function () {
            modifyPlaceholder();
            showSectionOnMobile();
            activateTopNav();
        });

        window.addEventListener('resize', function () {
            setTimeout(function () {
                modifyPlaceholder();
                showSectionOnMobile();
                stickyNavigation();
            }, 300); // Debounce resize event
        });

        window.addEventListener('scroll', function () {
            stickyNavigation();
        });

        // sticky navigation function
        const navbar = document.querySelector('.top-navigation-container');
        const sticky = navbar.offsetTop;

        function stickyNavigation() {
            if (window.innerWidth > 768) {

                if (window.pageYOffset >= sticky) {
                    navbar.classList.add("sticky");
                } else {
                    navbar.classList.remove("sticky");
                }
            } else {
                navbar.classList.remove("sticky");
            }
        }

        // function to modify placeholder
        function modifyPlaceholder() {
            const searchInput = document.querySelector('.header-01 .search-input');
            const isMobile = document.body.classList.contains('is-mobile');

            if (isMobile) {
                searchInput.setAttribute('placeholder', 'Find with our integrated AI copilot...');
            }
            else {
                searchInput.setAttribute('placeholder', 'Find it faster with our integrated AI copilot...');
            }
        }

        // function to show section on mobile
        function showSectionOnMobile() {
            const isMobile = document.body.classList.contains('is-mobile');
            if (!isMobile)
                return;

            const navbarToggler = document.querySelector('.navbar-toggler');
            navbarToggler.addEventListener('click', function () {
                const navCollapses = document.querySelectorAll('.nav-fade');
                navCollapses.forEach(function (navCollapse) {
                    navCollapse.classList.toggle('show');
                });
            });
        }

        // function to update header sibling height
        function updateHeaderSiblingHeight() {
            const isMobile = document.body.classList.contains('is-mobile');
            if (!isMobile)
                return;

            const header = document.querySelector('.header-01');
            const headerHeight = header.offsetHeight;
            const sibling = document.querySelector('.header-01+*')
            if (!sibling)
                return;
            sibling.style.paddingBlockStart = `${headerHeight}px`;
        }

        function activateTopNav() {
            const topNavigations = document.querySelectorAll('.top-navigation > ul > li');
            topNavigations.forEach(function (topNavigation) {
                topNavigation.addEventListener('mouseenter', function () {
                    topNavigations.forEach(function (nav) {
                        nav.classList.remove('active');
                    });

                    topNavigation.classList.add('active');

                    const subNavLevel1 = topNavigation.querySelector('nav > .subnav-level-1');
                    if (subNavLevel1) {
                        const subNavLevelItems = subNavLevel1.querySelectorAll('ul > li');
                        subNavLevelItems.forEach(function (subNavLevelItem) {
                            subNavLevelItem.addEventListener('mouseenter', function () {
                                subNavLevelItems.forEach(function (item) {
                                    item.classList.remove('active');
                                });
                                subNavLevelItem.classList.add('active');
                            });
                            subNavLevelItem.addEventListener('mouseleave', function () {
                                subNavLevelItem.classList.remove('active');
                            });
                        });
                    }

                });
                topNavigation.addEventListener('mouseleave', function () {
                    topNavigation.classList.remove('active');
                    const subNavLevel1 = topNavigation.querySelector('nav > .subnav-level-1');
                    if (subNavLevel1) {
                        const subNavLevelItems = subNavLevel1.querySelectorAll('ul > li');
                        subNavLevelItems.forEach(function (subNavLevelItem) {
                            subNavLevelItem.classList.remove('active');
                        });
                    }
                });
            });
        }
    </script>

