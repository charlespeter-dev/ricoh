        // sticky navigation function
        const navbar = document.querySelector('.top-navigation-container');
        const sticky = navbar.offsetTop;

        function stickyNavigation() {
            if (window.innerWidth > 768) {

                if (window.pageYOffset >= sticky) {
                    navbar.classList.add("sticky");
                } else {
                    navbar.classList.remove("sticky");
                }
            } else {
                navbar.classList.remove("sticky");
            }
        }

        // function to modify placeholder
        function modifyPlaceholder() {
            const searchInput = document.querySelector('.header-01 .search-input');
            const isMobile = window.matchMedia("(max-width: 768px)").matches;

            if (isMobile) {
                searchInput.setAttribute('placeholder', 'Find with our integrated AI copilot...');
            }
            else {
                searchInput.setAttribute('placeholder', 'Find it faster with our integrated AI copilot...');
            }
